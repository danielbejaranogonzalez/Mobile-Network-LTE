    private void obtenerPuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_obtenerPuntoActionPerformed
        // TODO add your handling code here:
        
        latGradosDecimal.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLatitudDecimal());
        longGradosDecimal.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLongitudDecimal());   
        controlador.getLibroDeProyectos().getMapa().getObtenerPunto().convertirLat_A_GradMinSeg();
        controlador.getLibroDeProyectos().getMapa().getObtenerPunto().convertirLong_A_GradMinSeg();
        
        DecimalFormat df = new DecimalFormat("##.###"); 
        
        latGrados.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLatitudGrados());
        latMinutos.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLatitudMinutos());
        latSegundos.setText("" + df.format(controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLatitudSegundos()));    
        hemisferioLat.setSelectedItem("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getHemisferioLatitud());
        
        longGrados.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLongitudGrados());
        longMinutos.setText("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLongitudMinutos());
        longSegundos.setText("" + df.format(controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getLongitudSegundos()));
        
        hemisferioLong.setSelectedItem("" + controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getHemisferioLongitud());
        
        elevacion = controlador.getLibroDeProyectos().getMapa().getObtenerPunto().getElevacion();
    }//GEN-LAST:event_obtenerPuntoActionPerformed


    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        
        if(!nombreEU.getText().equals("")&&!tipoEU.getSelectedItem().equals("")){
            
            // Si se ingresaron coordenadas geograficas en formato decimal
            if(decimal.isSelected()){
                controlador.getControladorVentanas().crearNuevoEUCoordenadasDecimales(Double.parseDouble(latGradosDecimal.getText()), 
                    Double.parseDouble(longGradosDecimal.getText()), elevacion, nombreEU.getText(), Float.parseFloat(altura.getValue().toString()), tipoEU.getSelectedItem().toString(), Float.parseFloat(sensibilidad.getValue().toString()), 
                                    tecnologia.getSelectedItem().toString());
                    this.dispose();
            // Si se ingresaron coordenadas geograficas en formato grados minutos y segundos
            }else if(gradosMinutosSegundos.isSelected()){
                try {
                    DecimalFormat formateador = new DecimalFormat("##.###");
                    Number segundosLatDouble = formateador.parse(latSegundos.getText());
                    Number segundosLongDouble = formateador.parse(longSegundos.getText());
                    
                    controlador.getControladorVentanas().crearNuevoEUCoordenadasGradMinSeg(Integer.parseInt(latGrados.getText()), Integer.parseInt(latMinutos.getText()), 
                                    (float)segundosLatDouble.doubleValue(),hemisferioLat.getSelectedItem().toString().charAt(0),Integer.parseInt(longGrados.getText()), 
                                    Integer.parseInt(longMinutos.getText()), (float)segundosLongDouble.doubleValue(), hemisferioLong.getSelectedItem().toString().charAt(0), elevacion,
                                    nombreEU.getText(), Float.parseFloat(altura.getValue().toString()), tipoEU.getSelectedItem().toString(), Float.parseFloat(sensibilidad.getValue().toString()), 
                                    tecnologia.getSelectedItem().toString());
                    this.dispose();
                    
                } catch (ParseException ex) {
                    Logger.getLogger(VentanaNuevoEquipoUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_botonAceptarActionPerformed


    private void encontrarSitioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encontrarSitioActionPerformed
        // TODO add your handling code here:
        String proyectoPredeterminado = controlador.getLibroDeProyectos().getProyectoPredeterminado();
        LinkedList <Proyecto> proyectos = controlador.getLibroDeProyectos().getProyectos();
        
        for(int i = 0; i < proyectos.size(); i++){
            
            if(proyectos.get(i).getNombre().equals(proyectoPredeterminado)){
                arraySitios = new String [proyectos.get(i).getGrupoSitios().size()];
                for(int j = 0; j < proyectos.get(i).getGrupoSitios().size(); j++){
                    
                    Sitio sitioActual = proyectos.get(i).getGrupoSitios().get(j);
                    if( sitioActual.getNombreSitio().equals(sitios.getSelectedItem().toString())) {
                        
                        nombreSitio.setText(sitioActual.getNombreSitio());
                        direccionSitio.setText(sitioActual.getDireccionSitio());
                        pais.setText(sitioActual.getPais());
                        ciudad.setText(sitioActual.getCiudad());
                        tipoSitio.setSelectedItem("" + sitioActual.getTipoSitio());
                        
                        latGrados.setText("" + sitioActual.getCoordenadaSitio().getLatitudGrados());
                        latMinutos.setText("" + sitioActual.getCoordenadaSitio().getLatitudMinutos());
                        latSegundos.setText("" + sitioActual.getCoordenadaSitio().getLatitudSegundos());
                        longGrados.setText("" + sitioActual.getCoordenadaSitio().getLongitudGrados());
                        longMinutos.setText("" + sitioActual.getCoordenadaSitio().getLongitudMinutos());
                        longSegundos.setText("" + sitioActual.getCoordenadaSitio().getLongitudSegundos());
                        latGradosDecimal.setText("" + sitioActual.getCoordenadaSitio().getLatitudDecimal());
                        longGradosDecimal.setText("" + sitioActual.getCoordenadaSitio().getLongitudDecimal());   
                        
                        hemisferioLat.setSelectedItem("" + sitioActual.getCoordenadaSitio().getHemisferioLatitud());
                        hemisferioLong.setSelectedItem("" + sitioActual.getCoordenadaSitio().getHemisferioLongitud());
                        cambiarEstadoCampos(false);
                    }
                }
            }
        }
    }//GEN-LAST:event_encontrarSitioActionPerformed


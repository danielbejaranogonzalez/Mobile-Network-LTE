    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        
        if(!nombreSitio.getText().equals("")&&!pais.getText().equals("")){
            
            // Si se ingresaron coordenadas geograficas en formato decimal
            if(decimal.isSelected()){
                controlador.getControladorVentanas().editarSitioCoordenadasDecimales(Double.parseDouble(latGradosDecimal.getText()),
                        Double.parseDouble(longGradosDecimal.getText()), sitios.getSelectedItem().toString(),nombreSitio.getText(), direccionSitio.getText(), 
                        pais.getText(), ciudad.getText(), tipoSitio.getSelectedItem().toString());
                this.dispose();
                // Si se ingresaron coordenadas geograficas en formato grados minutos y segundos
            }else if(gradosMinutosSegundos.isSelected()){
                try {
                    DecimalFormat formateador = new DecimalFormat("##.###");
                    Number segundosLatDouble = formateador.parse(latSegundos.getText());
                    Number segundosLongDouble = formateador.parse(longSegundos.getText());
                    
                    controlador.getControladorVentanas().editarSitioCoordenadasGradMinSeg(Integer.parseInt(latGrados.getText()), Integer.parseInt(latMinutos.getText()),
                            (float)segundosLatDouble.doubleValue(),hemisferioLat.getSelectedItem().toString().charAt(0),Integer.parseInt(longGrados.getText()),
                            Integer.parseInt(longMinutos.getText()), (float)segundosLongDouble.doubleValue(), hemisferioLong.getSelectedItem().toString().charAt(0),
                            sitios.getSelectedItem().toString(), nombreSitio.getText(), direccionSitio.getText(), pais.getText(), ciudad.getText(), tipoSitio.getSelectedItem().toString());
                    this.dispose();
                    
                } catch (ParseException ex) {
                    
                }
            }
        }
}//GEN-LAST:event_botonAceptarActionPerformed


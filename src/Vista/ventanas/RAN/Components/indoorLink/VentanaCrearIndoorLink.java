    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
       
      try{
            if(!nombreEnlaceIndoor.getText().equals("")&&!equiposRadio.getSelectedItem().toString().equals("")&&!slotsTarjetas.getSelectedItem().toString().equals("")&&
                    !antenas.getSelectedItem().toString().equals("")){

                String[] listaElementosAtenuacion = new String [listaAtenuadoresLinkActual.getModel().getSize()];
                for(int i = 0; i<listaAtenuadoresLinkActual.getModel().getSize(); i++){
                    listaElementosAtenuacion[i] = listaAtenuadoresLinkActual.getModel().getElementAt(i).toString();
                }

                controlador.getControladorVentanas().crearNuevoIndoorLink(nombreEnlaceIndoor.getText(), equiposRadio.getSelectedItem().toString(),
                        Integer.parseInt(slotsTarjetas.getSelectedItem().toString()), antenas.getSelectedItem().toString(), listaElementosAtenuacion ,nombreRAN, nombreENodeB);
                this.dispose();
            }
      }catch(NullPointerException NPE){
          JOptionPane.showMessageDialog(null, "'Existen campos vacio o valores incorrectos'","Error 002: NullPointerException",JOptionPane.ERROR_MESSAGE);
      }
}//GEN-LAST:event_botonAceptarActionPerformed


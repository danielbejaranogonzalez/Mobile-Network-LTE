    public VentanaCrearIndoorLink(java.awt.Frame parent, boolean modal, ControladorGeneral controlador, String nombreRAN, String nombreENodeB) {
        super(parent, modal); 
        this.controlador = controlador;
        this.nombreRAN = nombreRAN;
        this.nombreENodeB = nombreENodeB;
        initComponents();
        
        String[] arrayEquiposRadio = new String[0];
        String[] arrayAntenas = new String[0];
        String[] arrayAtenuadores = new String[0];
        DefaultListModel atenuadoresModel = new DefaultListModel();
        atenuadoresEnlaceModel = new DefaultListModel();
        
        LinkedList<Proyecto> proyectos = controlador.getLibroDeProyectos().getProyectos();
        String proyectoPredeterminadoActual = controlador.getLibroDeProyectos().getProyectoPredeterminado();
        for(int i=0; i < proyectos.size(); i++){
            //si el proyecto es predeterminado
             if(proyectos.get(i).getNombre().equals(proyectoPredeterminadoActual)){
                 for(int j=0; j < proyectos.get(i).getGrupoRANs().size(); j++){
                     if(proyectos.get(i).getGrupoRANs().get(j).getNombre().equals(nombreRAN)){
                         LinkedList <ENodeB> listaNodos = proyectos.get(i).getGrupoRANs().get(j).getGrupoNodosB();
                         for(int k=0; k < listaNodos.size(); k++){
                             if(listaNodos.get(k).getNombre().equals(nombreENodeB)){
                                 arrayEquiposRadio = new String[listaNodos.get(k).getRadios().size()];
                                 for(int l=0; l < listaNodos.get(k).getRadios().size(); l++){
                                     arrayEquiposRadio[l] = listaNodos.get(k).getRadios().get(l).getNombre();
                                 }    
                                 arrayAntenas = new String[listaNodos.get(k).getAntenas().size()];
                                 for(int l=0; l < listaNodos.get(k).getAntenas().size(); l++){
                                     arrayAntenas[l] = listaNodos.get(k).getAntenas().get(l).getNombreAntena();
                                 } 
                                 
                                 for(int l=0; l < listaNodos.get(k).getAtenuadores().size(); l++){
                                     atenuadoresModel.addElement(listaNodos.get(k).getAtenuadores().get(l).getNombreDispositivo());
                                 } 
                             }
                         }
                     }
                 }
             }
        }
        
        listaAtenuadoresENodeB.setModel(atenuadoresModel);
        equiposRadio.setModel(new DefaultComboBoxModel(arrayEquiposRadio));
        antenas.setModel(new DefaultComboBoxModel(arrayAntenas));
    }


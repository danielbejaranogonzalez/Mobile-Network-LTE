    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
       
        try{
            if(!nombreOutdoorLink.getText().equals("")&&!equipoUsuario.getSelectedItem().toString().equals("")&&!indoorLink.getSelectedItem().toString().equals("")){
                if(tecnologiaEnlace.getSelectedItem().toString().equals(tecnologiaEstacionMovil.getText())&&tecnologiaEnlace.getSelectedItem().toString().equals(tecnologiasEstacionBase.getText())){
                    controlador.getControladorVentanas().crearNuevoOutdoorLink(nombreOutdoorLink.getText(), tipoEnlace.getSelectedItem().toString(), tecnologiaEnlace.getSelectedItem().toString(),
                            equipoUsuario.getSelectedItem().toString(), RAN.getSelectedItem().toString(), eNodeB.getSelectedItem().toString(), indoorLink.getSelectedItem().toString());
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "'Existe incompatibilidad de tecnologias'","Error 010: Inconsistencia de Parametros",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "'Existen campos vacio o valores incorrectos'","Error 002: NullPointerException",JOptionPane.ERROR_MESSAGE);
            }
        }catch(NullPointerException NPE){
            JOptionPane.showMessageDialog(null, "'Existen campos vacio o valores incorrectos'","Error 002: NullPointerException",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed


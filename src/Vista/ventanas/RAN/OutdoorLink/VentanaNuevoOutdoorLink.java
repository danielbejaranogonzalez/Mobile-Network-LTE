    public VentanaNuevoOutdoorLink(java.awt.Frame parent, boolean modal, ControladorGeneral controlador) {
        super(parent, modal);
        this.controlador = controlador;
        initComponents();
        
        String[] arrayNombresRAN = null;
        String[] arrayNombresEU = null;
        LinkedList<Proyecto> proyectos = controlador.getLibroDeProyectos().getProyectos();
        String proyectoPredeterminadoActual = controlador.getLibroDeProyectos().getProyectoPredeterminado();
        for(int i=0; i < proyectos.size(); i++){
            //si el proyecto es predeterminado
             if(proyectos.get(i).getNombre().equals(proyectoPredeterminadoActual)){
                 arrayNombresRAN = new String[proyectos.get(i).getGrupoRANs().size()];
                 for(int j=0; j < proyectos.get(i).getGrupoRANs().size(); j++){
                     arrayNombresRAN[j] = proyectos.get(i).getGrupoRANs().get(j).getNombre();
                 }
                 arrayNombresEU = new String[proyectos.get(i).getGrupoEUs().size()];
                 for(int j=0; j < proyectos.get(i).getGrupoEUs().size(); j++){
                     arrayNombresEU[j] = proyectos.get(i).getGrupoEUs().get(j).getNombreEstacionMovil();
                 }
             }
        }
        try{
            RAN.setModel(new DefaultComboBoxModel(arrayNombresRAN));
        }catch(NullPointerException NPE){
            JOptionPane.showMessageDialog(null,"'No exiten ningun proyecto predeterminado'","Error 008: NullPointerException",JOptionPane.ERROR_MESSAGE);
        }
        try{
            equipoUsuario.setModel(new DefaultComboBoxModel(arrayNombresEU));
        }catch(NullPointerException NPE){
            //JOptionPane.showMessageDialog(null,"'No exiten ningun proyecto predeterminado'","Error 009: NullPointerException",JOptionPane.ERROR_MESSAGE);
        }
        
        tipoEnlace.setModel(new DefaultComboBoxModel(OutdoorLink.TIPOS_OUTDOOR_LINK_REDMOVIL));
        tipoEnlace.setSelectedItem("Uu");
        tecnologiaEnlace.setModel(new DefaultComboBoxModel(OutdoorLinkRAN.TECNOLOGIAS_OUTDOOR_LINK_RAN));
    }

